def add(a,b):
    """
    Somme deux nombres, le premier au deuxième.

    Arguments:
        float a: Premier addend.
        float b: Deuxième addend.

    Retour:
        float: Somme de a et b.
    """
    return a + b

def subtract(a,b):
    """
    Fait soustraction, a se voit soustraire b.

    Arguments:
        float a: Premier minuend.
        float b: Deuxième subtrahend.

    Retour:
        float: Différence de a avec b.
    """
    return b - a

def multiply(a,b):
    """
    Multiplie le nombre a avec le nombre b.

    Arguments
        float a: Première multiplicande.
        float b: Deuxième multiplicande.

    Retour:
        float: Produit de a et b.
    """
    return a * b

def divide(a,b):
    """
    Divise le nombre a avec le nombre b.

    Arguments:
        float a: Numérateur.
        float b: Dénominateur.

    Retour:
        float: quotient de a sur b.
    """
    return a / b
